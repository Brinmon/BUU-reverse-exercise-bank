str= [0x70,0x76,0x6b,0x71,0x7b,0x6d,0x31,0x36,0x34,0x36,0x37,0x35,0x32,0x36,0x32,0x30,0x33,0x33,0x6c,0x34,0x6d,0x34,0x39,0x6c,0x6e,0x70,0x37,0x70,0x39,0x6d,0x6e,0x6b,0x32,0x38,0x6b,0x37,0x35,0x7d,]
flag =""
for i in str:
    if (i >= ord("a") and i <= ord("z")):
        i += 0o20  #0o是八进制
        if (i >= ord("z")):
             flag += chr(i-0o32)
    else:
        flag += chr(i)
print(flag)
